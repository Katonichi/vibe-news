export function buildReportPrompt(topic) {
  const today = formatDateSlash();
  const sourceInfo = topic.sourceUrl
    ? `\n情報元: ${topic.sourceTitle || topic.sourceUrl}\nURL: ${topic.sourceUrl}`
    : '';

  return `あなたは技術リサーチと解説記事のプロフェッショナルです。以下のトピックについて、
読者の理解を深め好奇心を刺激する、読み応えのある調査レポートを作成してください。

トピック: ${topic.title}
概要: ${topic.summary}${sourceInfo}

## レポートの方針
- 元の記事・ニュース・論文の内容をベースにしつつ、**さらに一歩踏み込んだ深堀り**を行う
- 関連する技術的背景、歴史的経緯、競合他社の動向、業界の文脈を豊富に盛り込む
- 「なぜそうなったのか」「何が画期的なのか」「今後どう発展しうるか」を多角的に分析する
- 読者が「へぇ、そうだったのか！」と思えるような補足情報、裏話、意外な事実を積極的に含める
- 単なるニュース要約ではなく、テックコラム・解説記事のような厚みと面白さを持たせる

## レポート構成
1. **タイトルと調査日**（${today}）
2. **エグゼクティブサマリー**（3-5文で核心を伝える）
3. **ニュースの概要と要点**（元の記事・発表の内容を正確に整理）
4. **背景と経緯の深堀り**（なぜ今このタイミングなのか、ここに至るまでの歴史や伏線）
5. **技術的詳細**（具体的な仕組み、アーキテクチャ、使用技術。図解的な説明も歓迎）
6. **競合・関連プレイヤーの動向**（同じ領域で他社は何をしているか、比較や位置づけ）
7. **業界への影響と今後の展望**（短期・中長期でどんな変化が起きうるか）
8. **知っておくと面白い関連トピック**（コラム的な補足。意外なつながり、裏話、豆知識など）
9. **実務への応用可能性**（開発者・企業としてどう活用できるか、具体的なアクション）
10. **関連リソースと参考情報**（さらに深く知りたい人向けのリンク・資料）
11. **まとめ**

## 執筆ルール
- Markdown形式で、見出し・箇条書き・表・引用を適切に使用する
- 専門用語には初出時に簡単な説明を付記する
- 情報は最新かつ正確であることを最重視する
- 数字・データ・具体例を積極的に含め、抽象的な記述を避ける
- 読者が飽きないよう、硬すぎず適度にカジュアルなトーンも交える`;
}

